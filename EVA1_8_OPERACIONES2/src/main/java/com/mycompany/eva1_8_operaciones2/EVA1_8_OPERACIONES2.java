public class EVA1_8_OPERACIONES2 {

    public static void main(String[] args) {
        String Nombre = "Andres";
        String Apellido = "Campos";
        String NombreCom;
        //Cadena de texto, la operacion se llama concatenacion --> unir cadena de texto
        //El operador de concatenacion es el + cuan un simbolo tiene varias funciones se llama sobrecarga (de operadores)
        NombreCom = Nombre +" "+ Apellido;
        System.out.println(NombreCom);
        
        System.out.println("El Nombre es: "+Nombre);
        System.out.println("El Apellido es: "+Apellido);
        System.out.println("EL Nombre completo es: "+NombreCom);
    }
}
